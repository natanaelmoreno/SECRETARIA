    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.txtnombre.getText() == null) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un nombre", "AVISO DEL SISTEMA", 1);

        } else if (this.txtcarnet.getText() == null) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un carnet", "AVISO DEL SISTEMA", 2);

        } else if (this.txtdireccion.getText() == null) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar una dirreccion de domicilio", "AVISO DEL SISTEMA", 2);

        } else if (this.txttelefono.getText() == null) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un  telefono", "AVISO DEL SISTEMA", 2);

        } else {

            llamar.insertarAlumno(this.txtcarnet.getText(), this.txtnombre.getText(), this.txtdireccion.getText(), Integer.parseInt(this.txttelefono.getText()), this.txtlugar.getText(), Integer.parseInt(this.txtteltra.getText()));
            // llamar.insertarCarrera(this.txtcodigocarrera.getText(),this.cmbcarrera.getSelectedIndex(),Integer.parseInt(this.txtplan.getText()));
            llamar.insertarInscripcion(Integer.parseInt(this.txtcodigo.getText()), Integer.parseInt(this.txtaniaca.getText()), Integer.parseInt(this.txtciclo.getText()), this.txtuv.getText(), Integer.parseInt(this.txtingreso.getText()), Integer.parseInt(this.txtegreso.getText()), this.txtnombre.getText(), String.valueOf(this.cmbasesor.getSelectedItem()), this.txtcodigocarrera.getText(), this.txttema.getText(), this.txtinicio.getText(), this.txtfinal.getText(), this.cmbasesor.getSelectedIndex(), this.cmbdirector.getSelectedIndex(), this.txtcarnet.getText());
            JOptionPane.showMessageDialog(null, "ingreso exitoso");

        }

        limpiar();
        bloquear();
    }//GEN-LAST:event_jButton1ActionPerformed

