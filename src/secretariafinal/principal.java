    private void txtcicloKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcicloKeyPressed
        char c = evt.getKeyChar();
        int car = evt.getKeyChar();
        if (Character.isLetter(c) || (car >= 32 && car <= 47)) {
            JOptionPane.showMessageDialog(null, "solo introduzca numeros");
            txtciclo.setText(null);
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            numero = Integer.parseInt(txtciclo.getText());
            if (numero < 0) {
                JOptionPane.showMessageDialog(null, "debe estar ser mayor o igual a cero");
                txtciclo.setText("");
            } else {
                txtaniaca.requestFocus();
            }
        }
    }//GEN-LAST:event_txtcicloKeyPressed

    private void btnnuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnuevoActionPerformed
        // TODO add your handling code here:rs = null;
        desbloquear();
        rs = llamar.contarRegistros5();
        try {
            while (rs.next()) {
                cantidad = rs.getInt(1);
                if (cantidad != 0) {
                    rs = null;
                    //mÃ©todo en clase Clientes
                    rs = llamar.mayorRegistro5();
                    while (rs.next()) {
                        mayor = rs.getInt(1) + 1;
                        //recuerde que debe completar 5 digitos
                        if (mayor < 10) {
                            txtcodigo.setText("0" + mayor);
                        } else if (mayor < 100) {
                            this.txtcodigo.setText("00" + mayor);
                        } else if (mayor < 1000) {
                            txtcodigo.setText("0" + mayor);
                        } else {
                            txtcodigo.setText("" + mayor);
                        }
                    }
                    txtnombre.requestFocus();
                } else {
                    txtcodigo.setText("0" + 1);
                    txtnombre.requestFocus();
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "AVISO DEL SISTEMA", 0);
        }
    }//GEN-LAST:event_btnnuevoActionPerformed

